function withHydroTrack(country: CountryProfile): CountryProfile {
  if (!hydroCountryCodes.has(country.code) || country.tracks.includes("hydro")) {
    return country;
  }

  return {
    ...country,
    tracks: [...country.tracks, "hydro"],
  };
}

