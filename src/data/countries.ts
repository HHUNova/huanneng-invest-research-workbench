interface CountrySeed {
  code: string;
  name: string;
  englishName: string;
  region: Region;
  coordinates: [number, number];
  riskLevel: RiskLevel;
  attractionScore: number;
  installedGW: number;
  newCapacityGW2026: number;
  investmentBillionUSD2026: number;
  benchmarkTariffUSDPerMWh: number;
  chineseProjects: number;
  irrRange: [number, number];
  tracks: Track[];
  capex: number;
  hours: number;
  tax: number;
  loan: number;
  discount: number;
  curtailment?: number;
  riskScores: [number, number, number, number, number];
}

function buildCountry(seed: CountrySeed): CountryProfile {
  return {
    code: seed.code,
    name: seed.name,
    englishName: seed.englishName,
    region: seed.region,
    coordinates: seed.coordinates,
    riskLevel: seed.riskLevel,
    attractionScore: seed.attractionScore,
    installedGW: seed.installedGW,
    newCapacityGW2026: seed.newCapacityGW2026,
    investmentBillionUSD2026: seed.investmentBillionUSD2026,
    benchmarkTariffUSDPerMWh: seed.benchmarkTariffUSDPerMWh,
    chineseProjects: seed.chineseProjects,
    irrRange: seed.irrRange,
    tracks: seed.tracks,
    defaults: trackDefaults(
      defaults({
        totalInvestmentMillionUSD: seed.capex,
        utilizationHours: seed.hours,
        tariffUSDPerMWh: seed.benchmarkTariffUSDPerMWh,
        corporateTaxRate: seed.tax,
        curtailmentRate: seed.curtailment ?? 0.025,
        loanInterestRate: seed.loan,
        discountRate: seed.discount,
      }),
    ),
    risk: {
      dimensions: riskDimensions(seed.riskScores),
      bit: `已预留中国与${seed.name}投资保护条款核验位，正式尽调需逐条校验征收补偿、最惠国待遇和争端解决安排。`,
      dta: `已预留避免双重征税协定和预提税参数接口，当前税率为MVP阶段基准估算。`,
      sinosure: `${seed.name}样本纳入中国信保国别风险提示位，承保结论需结合购电方信用、主权支持和外汇安排复核。`,
      summary: `${seed.name}已纳入扩展静态样本库，可用于市场筛选、风险评分和收益测算联动。`,
    },
  };
}

